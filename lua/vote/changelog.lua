<h2>Version 1.1.2 (13/08/2017):</h2>

- Added a fellow for the Jackal: <b> Sidekick </b> <br>
<div style=" text-align: left; margin-left: 15px;"> - Jackal has new Item in shop, The "Sidekick Deagle" <br>
